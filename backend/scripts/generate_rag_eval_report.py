"""Generate markdown report and trends from RAG eval manifest runs."""

from __future__ import annotations

import argparse
import json
import sys
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

BACKEND_ROOT = Path(__file__).resolve().parents[1]
REPO_ROOT = Path(__file__).resolve().parents[2]
if str(BACKEND_ROOT) not in sys.path:
	sys.path.insert(0, str(BACKEND_ROOT))

from scripts.run_rag_eval_manifest import run_manifest


def _load_history(path: Path) -> list[dict[str, Any]]:
	"""Load JSONL run history if it exists."""
	if not path.exists():
		return []
	entries: list[dict[str, Any]] = []
	for line in path.read_text(encoding="utf-8").splitlines():
		line = line.strip()
		if not line:
			continue
		try:
			entries.append(json.loads(line))
		except json.JSONDecodeError:
			continue
	return entries


def _append_history(path: Path, entry: dict[str, Any]) -> None:
	"""Append one run entry to JSONL history."""
	path.parent.mkdir(parents=True, exist_ok=True)
	with path.open("a", encoding="utf-8") as handle:
		handle.write(json.dumps(entry, ensure_ascii=True) + "\n")


def _build_markdown(
	entry: dict[str, Any],
	history: list[dict[str, Any]],
	gates: dict[str, Any],
	history_limit: int,
) -> str:
	"""Build markdown report containing latest run and per-case trends."""
	timestamp = entry["timestamp_utc"]
	results = entry["results"]
	total = entry["total"]
	passed = entry["passed"]
	failed = entry["failed"]

	lines: list[str] = []
	lines.append("# RAG Eval Report")
	lines.append("")
	lines.append(f"Generated at: {timestamp}")
	lines.append("")
	lines.append("## Latest Run")
	lines.append("")
	lines.append(f"- Enabled cases: {total}")
	lines.append(f"- Passed: {passed}")
	lines.append(f"- Failed: {failed}")
	lines.append(
		f"- Quality gates: term_coverage >= {float(gates.get('term_coverage_min', 0.0)):.2f}, "
		f"source_recall_at_k >= {float(gates.get('source_recall_at_k_min', 0.0)):.2f}"
	)
	lines.append("")
	lines.append("| Case ID | Status | Term Coverage | Source Recall@K | Retrieved |")
	lines.append("|---|---:|---:|---:|---:|")
	for item in results:
		term = "n/a" if item.get("term_coverage") is None else f"{float(item['term_coverage']):.3f}"
		source = "n/a" if item.get("source_recall_at_k") is None else f"{float(item['source_recall_at_k']):.3f}"
		lines.append(
			f"| {item['case_id']} | {item['status'].upper()} | {term} | {source} | {int(item.get('retrieved_count', 0))} |"
		)

	window = history[-history_limit:]
	status_by_case: dict[str, list[str]] = defaultdict(list)
	for run in window:
		for item in run.get("results", []):
			status_by_case[str(item.get("case_id", "unknown"))].append(str(item.get("status", "unknown")))

	lines.append("")
	lines.append(f"## Trend Snapshot (Last {len(window)} Runs)")
	lines.append("")
	lines.append("| Case ID | Pass Rate | Last 5 Statuses |")
	lines.append("|---|---:|---|")
	for case_id in sorted(status_by_case):
		statuses = status_by_case[case_id]
		passes = sum(1 for status in statuses if status == "pass")
		pass_rate = passes / len(statuses) if statuses else 0.0
		recent = ", ".join(status.upper() for status in statuses[-5:])
		lines.append(f"| {case_id} | {pass_rate:.0%} | {recent} |")

	lines.append("")
	lines.append("## Notes")
	lines.append("")
	lines.append("- This file is generated by backend/scripts/generate_rag_eval_report.py.")
	lines.append("- Run history source: logs/rag_eval_history.jsonl.")
	lines.append("- Use this report to track ingestion-quality regression before and after corpus updates.")
	lines.append("")
	return "\n".join(lines)


def main() -> int:
	"""Execute manifest cases, persist history, and write markdown report."""
	parser = argparse.ArgumentParser(description="Generate markdown report from RAG eval manifest")
	parser.add_argument(
		"--manifest",
		type=Path,
		default=REPO_ROOT / "rag" / "knowledge" / "eval_manifest.json",
		help="Path to eval_manifest.json",
	)
	parser.add_argument(
		"--report",
		type=Path,
		default=REPO_ROOT / "docs" / "rag-eval-report.md",
		help="Output markdown report path",
	)
	parser.add_argument(
		"--history",
		type=Path,
		default=REPO_ROOT / "logs" / "rag_eval_history.jsonl",
		help="JSONL run history path",
	)
	parser.add_argument("--top-k", type=int, default=None, help="Override top_k for all enabled cases")
	parser.add_argument("--history-limit", type=int, default=10, help="Number of recent runs to summarize")
	args = parser.parse_args()

	manifest_path = args.manifest if args.manifest.is_absolute() else (Path.cwd() / args.manifest).resolve()
	if not manifest_path.exists():
		print(f"Manifest file not found: {manifest_path}")
		return 2

	results, gates = run_manifest(manifest_path, top_k_override=args.top_k)
	passed = sum(1 for item in results if item.status == "pass")
	failed = sum(1 for item in results if item.status != "pass")
	entry = {
		"timestamp_utc": datetime.now(timezone.utc).isoformat(),
		"manifest": str(manifest_path),
		"total": len(results),
		"passed": passed,
		"failed": failed,
		"gates": gates,
		"results": [
			{
				"case_id": item.case_id,
				"status": item.status,
				"term_coverage": item.term_coverage,
				"source_recall_at_k": item.source_recall_at_k,
				"retrieved_count": item.retrieved_count,
				"detail": item.detail,
			}
			for item in results
		],
	}

	history_path = args.history if args.history.is_absolute() else (Path.cwd() / args.history).resolve()
	history_before = _load_history(history_path)
	_append_history(history_path, entry)
	history_after = history_before + [entry]

	report_path = args.report if args.report.is_absolute() else (Path.cwd() / args.report).resolve()
	report_path.parent.mkdir(parents=True, exist_ok=True)
	report = _build_markdown(entry, history_after, gates, max(1, int(args.history_limit)))
	report_path.write_text(report, encoding="utf-8")

	print(f"Wrote report: {report_path}")
	print(f"History entries: {len(history_after)}")
	print(f"Latest run: {passed} passed, {failed} failed, {len(results)} enabled")
	return 1 if failed else 0


if __name__ == "__main__":
	raise SystemExit(main())
